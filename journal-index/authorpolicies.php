            <div class="left-subheading">Style guidelines</div>
            <div>
                <p>All manuscripts must adhere to the following style guidelines, otherwise they will be rejected outright. Please see the example submission (HYPERLINK XX) and submission template (HYPERLINK XX). Manuscript contents for each article type can be found under the relevant article in article policies.</p>
            </div>
            <hr>

            <div class="left-subheading">Title page</div>
            <div>
                <p>Please see the submission template (HYPERLINK XX).</p>
                <p>Every manuscript must contain a title page, which should include:</p>
                <ul>
                    <li>Article title</li>
                    <li>Author names in order of authorship.</li>
                    <li>Author information: List of department, institution, city and country for each author. Each unique department should be numbered and included as superscript AFTER the relevant author.</li>
                    <li>Corresponding author contact details: full name, institution address, email, telephone number of the lead author.</li>
                    <li>Running title: a short version of the full title (to go at the top of each journal page).</li>
                    <li>Field of research: basic science: immunology, case study, systematic review etc.</li>
                    <li>Sources of support: including grants or other support that facilitated completion of the work.</li>
                    <li>Conflicts of interest</li>
                    <li>Keywords: 3-5 keywords that best represent the article. These are used for online search engines.</li>
                    <li>Number of figures: total number of figures (maximum 5).</li>
                    <li>Number of tables: total number of tables (maximum 3).</li>
                    <li>Word count, excluding title page, abstract, references, figures and tables.</li>
                </ul>
            </div>
            <hr>

            <div class="left-subheading">Abstract</div>
            <div>
                <p>Please see the submission template (HYPERLINK XX).</p>
                <p>The abstract should contain the following:</p>
                <ul>
                    <li>Introduction</li>
                    <li>Materials and methods</li>
                    <li>Results</li>
                    <li>Conclusions</li>
                </ul>
            </div>
            <hr>

            <div class="left-subheading">Introduction</div>                        
            <div class="indent">
                <div>
                    <p>The introduction should provide context and background for the study. For original research it should contain a hypothesis and aim(s). For audit it should contain a standard(s) and aim(s). Do not include any information from materials and methods, results, discussions, or conclusions in the aims.</p>
                </div>
            </div>
            <hr>

            <div class="left-subheading">Materials and Methods</div>                        
            <div class="indent">
                <div>
                    <p>The methods section should describe how the study/research was performed. Any materials and/or equipment should be identified followed by manufacturer’s name and city in parentheses. References to established methods should be included, less well-known methods should be described in text, with reasons for using these methods. Generic names should be used for drugs, with dose and route of administration included.</p>
                </div>
            </div>
            <hr>

            <div class="left-subheading">Results</div>                        
            <div class="indent">
                <div>
                    <p>The results section should highlight important findings from the study. Results should be presented in a logical sequence. Do not repeat details found in the methods or introduction. It should include minimal discussion of the results.</p>
                </div>
            </div>
            <hr>

            <div class="left-subheading">Discussion</div>                        
            <div class="indent">
                <div>
                    <p>The discussion should emphasise new and important aspects of the study. Authors should discuss the relative importance of the findings in the context of the available literature and the potential for future research or clinical practice.</p>
                    <p>Authors should avoid repetition of information contained within the results section. However, a brief description of the main findings may be required, followed by potential mechanisms or explanations for these findings.</p>
                    <p>Authors should avoid over exaggerating the importance of their results, and should not make claims that are not supported up by their results or the literature.</p>
                </div>
            </div>
            <hr>

            <div class="left-subheading">Conclusions</div>                        
            <div class="indent">
                <div>
                    <p>Conclusions should be linked to the aims of the study. Authors should avoid over exaggerated or unsupported conclusions.</p>
                </div>
            </div>
            <hr>

            <div class="left-subheading">References</div>                        
            <div class="indent">
                <div>
                    <p>Please follow the [LINK]style guidelines[/LINK] for references.</p>
                </div>
            </div>
            <hr>

            <div class="left-subheading">Resubmitting revised manuscripts</div>                        
            <div class="indent">
                <div>
                    <p>Following the peer review process, authors may be asked to re-submit a revised/amended manuscripts. As part of the peer review process authors may receive a document which will suggest changes. ALL suggested changes should be made; authors may make a rebuttal stating why certain suggested changes should not be made. If this is the case the Section Editor, Deputy Editor, or Editor-in-Chief will review the rebuttal and make a final decision on the suggested change in question.</p>
                    <p>Once the author has made the suggested changes the authors should submit an amended document AND an amended document compared to the original document (so that reviewers can see that the suggested changes have been made). This can be done by selecting Tools-->Track Changes-->Compare Documents-->Then select the original document and the amended document.</p>
                </div>
