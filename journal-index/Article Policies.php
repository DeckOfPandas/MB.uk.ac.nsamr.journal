					</ul>
			</div>	
		<div class="p-subheading">Case Quiz </div>
			<div>
				<p>Short quiz based around a case. The description of case should introduce the case and provide the background information for the questions. The description should not include the answers to the questions. 2-5 questions are then posed by the author relating to the case. The answers and explanations to these should then be answered in the section after the questions.</p>
				<p>Structure:<p/>
					<ul>
						<li>No abstract.</li>
						<li>500 words, including a description of the case followed by 2-5 questions with answers and explanations</li>
						<li>Maximum 5 references.</li>
						<li>To include: Title Page, Case Description, Questions, Answers and Explanations, References.</li>
						<li>Layout template can be found here (XX HYPERLINK)</li>
						<li>This should be submitted as a "case report" during the submission process.<li>
					</ul>
			</div>			
		<div class="p-subheading">Case Report or series and review </div>
			<div>
				<p>Synopsis of a single patient case or series of patient cases with a relevant literature review with discussion centered around noteworthy aspects of the case.</p>
				<p>Structure:<p/>
					<ul>
						<li>250 word abstract.</li>
						<li>2000-4000 words.</li>
						<li>Maximum 40 references.</li>
						<li>To include:Title Page, Introduction, Case Description(s) and Literature Review, Discussion, Learning Points, Conclusions, References. </li>
						<li>Layout template can be found here (XX HYPERLINK)</li>
						<li>This should be submitted as a "case report" during the submission process.<li>
					</ul>
			</div>	
		<div class="p-subheading">View points </div>
			<div>
				<p>JNSAMR welcomes articles which address any topic relating to medical research, medicine, public health policy, ethics, or medical law, which the author(s) feels are worthy of discussion.</p>
