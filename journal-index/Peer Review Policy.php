<div class="row">
    <div class="col-sm-12">
        <div class="left-heading">Peer Reviewer Rating</div>
        <div>
			<p>Peer reviewers will be rated by the appropriate section editor after each completed review.</p>
			<p>Reviewers are assessed on communication, timeliness, and review content.</p>
			<p>The rating scale is from 0 to 5.</p>
				<ul>
					<li>For a rating of 5, reviewers must ensure prompt communication with the Section Editor. The review will be accepted within 48 hours, and completed within two weeks. The review will adhere to JNSAMR peer review guidelines, and will be written in a concise well-written style that requires little revision from Section Editors.</li>
					<li>For a rating of 4, reviewers must ensure prompt communication with the Section Editor. The review will be accepted within 48 hours, and completed within two weeks. The review will adhere to JNSAMR peer review guidelines, and will be written in a concise well-written style that may require some revision from Section Editors.</li>
					<li>For a rating of 3, reviewers must ensure prompt communication with the Section Editor. The review will be accepted within 48 hours, and completed within two weeks. The review will adhere to JNSAMR peer review guidelines, but requires revision from Section Editors.</li>
					<li>For a rating of 2, reviewer communication is poor. The review is completed within two weeks. The review may not adhere to JNSAMR peer review guidelines, or requires substantial revision from Section Editors.</li>
					<li>For a rating of 1, reviewer communication is poor or review incomplete. The review is completed within 3 weeks.</li>
					<li>For a rating of 0, reviewer communication is poor. The review is not completed within the maximum of 3 weeks.</li>
				</ul>
			<p>Any non-professional behaviour will be automatically awarded a rating of 0, with review by the Editor-in-Chief.</p>
    </div>                       
