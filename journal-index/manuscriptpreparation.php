                    </div>
                </div>


                <div class="panel-heading" role="tab" id="in">
                    <h4 class="panel-title">
                        <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse-in" aria-expanded="false" aria-controls="in">
                            Introduction
                        </a>
                    </h4>
                </div>
                <div id="collapse-in" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="in">
                    <div class="panel-body">
                        <p>The introduction should provide context and background for the study. For original research it should contain a hypothesis and aim(s). For audit it should contain a standard(s) and aim(s). Do not include any information from materials and methods, results, discussions, or conclusions in the aims.</p>
                    </div>
                </div>


                <div class="panel-heading" role="tab" id="mam">
                    <h4 class="panel-title">
                        <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse-mam" aria-expanded="false" aria-controls="mam">
                            Materials and methods
                        </a>
                    </h4>
                </div>
                <div id="collapse-mam" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="mam">
                    <div class="panel-body">
                        <p>The methods section should describe how the study/research was performed. Any materials and/or equipment should be identified followed by manufacturerâ€™s name and city in parentheses. References to established methods should be included, less well-known methods should be described in text, with reasons for using these methods. Generic names should be used for drugs, with dose and route of administration included.</p>
                    </div>
                </div>


                <div class="panel-heading" role="tab" id="res">
                    <h4 class="panel-title">
                        <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse-res" aria-expanded="false" aria-controls="res">
                            Results
                        </a>
                    </h4>
                </div>
                <div id="collapse-res" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="res">
                    <div class="panel-body">
                        <p>The results section should highlight important findings from the study. Results should be presented in a logical sequence. Do not repeat details found in the methods or introduction. It should include minimal discussion of the results.</p>
                    </div>
                </div>


                <div class="panel-heading" role="tab" id="dis">
                    <h4 class="panel-title">
                        <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse-dis" aria-expanded="false" aria-controls="dis">
                            Discussion
                        </a>
                    </h4>
                </div>
                <div id="collapse-dis" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="dis">
                    <div class="panel-body">
                        <p>The discussion should emphasise new and important aspects of the study. Authors should discuss the relative importance of the findings in the context of the available literature and the potential for future research or clinical practice.</p>
                        <p>Authors should avoid repetition of information contained within the results section. However, a brief description of the main findings may be required, followed by potential mechanisms or explanations for these findings.</p>
                        <p>Authors should avoid over exaggerating the importance of their results, and should not make claims that are not supported up by their results or the literature.</p>
                    </div>
                </div>


                <div class="panel-heading" role="tab" id="con">
