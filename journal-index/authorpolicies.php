        <div>
            <p>All submissions to JNSAMR and all submission content must be the sole work of stated authors. The submitted work must not be published elsewhere, currently under consideration by any other journals, or be submitted to any other journals for consideration after submission to JNSAMR.</p>
            <p>Citations must be included for all submitted work following JNSAMR citation guidelines.</p>
            <p>Plagiarism and absence of citation of the ideas or content of others' work is a breach of author ethics and will not be tolerated by JNSAMR.</p>
