<div class=”row”>
		<div class="left-heading">Style Guidelines</div>

		<div class="p-subheading"><b>Format </b></div>
			<div>
					<ul>
						<li>All manuscripts should be submitted as a Word document (.doc, or .docx). PDF submission will be rejected outright.</li>
						<li>All text must be submitted in Arial, 12-point font, automatic colour, double spaced, and left aligned.</li>
						<li>Manuscript margins should be 2.54cm, and all pages should be numbered consecutively including the title page.</li>
						<li>Line number must be included (Format-->Document-->Layout-->Line numbers-->Continuous).</li>
						<li>Remove all hidden text headers or footers from your file before submission.</li>
					</ul>
					
	<div class="space20"></div> 
				<p>All manuscripts should be presented in the following order:</p>	
					<ol>
						<li>Title page.</li>
						<li>Abstract or summary (if appropriate).</li>
						<li>Main text.</li>
						<li>Headings and subheadings should follow the following order (BOLD CAPITALS, bold lower case, plain text, italics).</li>
						<li>Acknowledgements.</li>
						<li>Appendices.</li>
						<li>References.</li>
					</ol>
			</div>

			<div class="space20"></div> 
		<div class="p-subheading"><b>Style</b> </div>
			<div>
				<p>Abbreviations and symbols must be standard.</p>
				<p>Acronyms must be written in full when first used. Authors should then use the acronym for the rest of the manuscript.</p>
				<p>Acronyms should be used sparingly and only if the full phrase would be used more than 3 times in the manuscript.</p>
				<p>Drugs should be given their generic name whenever possible.</p>
			</div>	
			<div class="space20"></div> 

		<div class="p-subheading"><b>Figures/Illustrations</b> </div>
			<div>
				<p>Images must be embedded in the manuscript AND uploaded as separate files under<br>“Supplementary_File_Figure[FIGURE IN TEXT CITATION NUMBER]”</p>
				<p>Images must in the TIFF format with a resolution of 600 dpi.</p>
				<p>Images must be cited in the main text in numerical order.</p>
				<p>Figure legends should include a short title and must be below the figure they refer to.</p>
				<p>Colour images are encouraged.</p>
				<p>Histograms and graphs should be presented in a simple, two-dimensional format, with no background grid.</p>
				<p>All axis titles for graphs should include units in parentheses where appropriate.</p>
			</div>		

			<div class="space20"></div> 
		<div class="p-subheading"><b>Tables</b></div>
			<div>
				<p>Tables should be cited in text in numerical order.</p>
			</div>			

			<div class="space20"></div> 
		<div class="p-subheading"><b>Multimedia Files</b></div>
			<div>
				<p>Video files should be submitted as .WMF or .AVI formats, .FLV, .Mov and .MP4 files are also acceptable.</p>
				<p>Videos should be cited in text in numerical order.</p>
				<p>Videos should be uploaded under “Supplementary_File_Video[VIDEO IN TEXT CITATION NUMBER]”</p>
			</div>	

<div class="space20"></div> 
		<div class="p-subheading"><b>Permissions</b></div>
			<div>
				<p>Authors should avoid using any materials that have been published elsewhere, e.g. figures, tables, or videos.</p>
				<p>If authors wish to include any material published elsewhere, permission from the copyright holder (author or publisher) must be obtained.</p>
				<p> you'll find me here if you're a good deputey editor</p>
				<p>This includes any of your own previously published work, if you are not the copyright holder.</p>
			</div>	

	<div class="space20"></div> 
		<div class="p-subheading"><b>References</b></div>
			<div>
				<p>Authors are responsible for the accuracy of cited references.</p>
				<p>Authors must check all references before the manuscript is submitted.</p>
			</div>		

			<div class="space20"></div> 
		<div class="p-subheading"><b>In text Citation</b></div>
			<div>
				<p>The APA referencing style should be used for all citations.</p>
				<p>An explanation of the APA style can be found here: </p>
				<p>Unofficial references should be avoided but, if deemed necessary by the author, they should be cited in parentheses.</p>
			</div>		

			<div class="space20"></div> 
		<div class="p-subheading"><b>Digital Object Identifier (DOI)</b></div>
			<div>
				<p>A DOI is a unique identifier for an online manuscript. The DOI for each citation should be included after the reference.</p>
			</div>	

			<div class="space20"></div> 
		<div class="p-subheading"><b>ORCID</b></div>
			<div>
				<p>ORCID (http://orcid.org/) is a unique online identifier for authors. It is used to distinguish authors.</p>
				<p>All students named as authors must register an ORCID account and their ORCID number should be included after their name on the title page in parentheses.</p>
			</div>	
