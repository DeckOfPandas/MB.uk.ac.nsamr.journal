        <div class="left-heading">Peer Review Process</div>
        <ol>
			<li>Article submitted.</li>
			<li>Editor-in-chief/Deputy editor evaluates whether article can be published.</li>
			<li>Non-publishable articles are sent to JNSAMR advisory board member for review with recommendation to reject.</li>
			<li>Publishable articles are sent to the appropriate Section Editor.</li>
			<li>The Section Editor sends the unblinded article to two or three student reviewers.</li>
			<li>Reviewers have 48 hours to respond, otherwise an alternative reviewer is selected.</li>
			<li>Reviewers have 2 weeks to complete their peer review.</li>
			<li>The Section Editor then reviews the article and comments to compile a peer review summary and letter to the author.</li>
			<li>The section editor sends the peer review summary and author letter to the Editor-in-Chief with a recommendation to ‘accept with no revisions’, ‘accept with minor revisions’, ‘revise and resubmit’, or ‘reject with no resubmission’.</li>
			<li>The Editor-in-Chief responds to the author with the letter, peer-review summary, and final decision.</li>
			<li>JNSAMR aims to respond to authors within one month of article submission.</li>
			<li>If revisions are required the author then has one month to make the suggested changes and resubmit the article.</li>
			<li>Articles that receive the editorial decision of ‘accept with minor revisions’ are reviewed by original reviewers.</li>
			<li>Articles that receive the editorial decision of ‘revise and resubmit’ re-enter the normal peer-review process.</li>
		</ol>
